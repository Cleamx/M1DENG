    checkCollision(x, y) {
        // Convertir les coordonn√©es en indices de la grille
        const gridX = Math.floor(x / this.tileSize);
        const gridY = Math.floor(y / this.tileSize);

        if (gridX < 0 || gridX >= this.grid[0].length || gridY < 0 || gridY >= this.grid.length) {
            return true;
        }

        const cellValue = this.grid[gridY][gridX];

        return cellValue === 1;
    }

    draw(ctx) {
        ctx.fillStyle = 'blue';
        for (let y = 0; y < this.grid.length; y++) {
            for (let x = 0; x < this.grid[y].length; x++) {
                if (this.grid[y][x] === 1) {
                    ctx.fillRect(
                        x * this.tileSize,
                        y * this.tileSize,
                        this.tileSize,
                        this.tileSize
                    );
                }
            }
        }
    }
